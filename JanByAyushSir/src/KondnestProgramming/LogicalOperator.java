package KondnestProgramming;

public class LogicalOperator {
	public static void main(String[] args) {
		System.out.println(true || true);  //LOgical OR
		System.out.println(true || false);  //LOgical OR
		System.out.println(false || true);    //LOgical OR
		System.out.println(false || false);    //LOgical OR
		System.out.println("===--------------------------------new Line ----------------------==--=-=--=--==");
		
		System.out.println(true && true);    //LOgical AND
		System.out.println(true && false);   //LOgical AND
		System.out.println(false && true);    ////LOgical AND
		System.out.println(false && false);   //LOgical AND
	}
}
		
